#include <stdio.h>

#include "Point2D.h"

// *****************************************************************************
// *****************************************************************************
Point2D::Point2D(double x, double y) {
    pos[0] = x;
    pos[1] = y;
}

// *****************************************************************************
// *****************************************************************************
Point2D::~Point2D() {

}

// *****************************************************************************
// *****************************************************************************
double & Point2D::operator () (int i) {
    return pos[i];
}

// *****************************************************************************
// *****************************************************************************
double Point2D::operator () (int i) const {
    return pos[i];
}

// *****************************************************************************
// *****************************************************************************
double & Point2D::operator [] (int i) {
    return pos[i];
}

// *****************************************************************************
// *****************************************************************************
Point2D Point2D::operator *(double other) const {
    return Point2D(pos[0] * other, pos[1] * other);
}

// *****************************************************************************
// *****************************************************************************
Point2D Point2D::operator /(double other) const {
    return Point2D(pos[0] / other, pos[1] / other);
}

// *****************************************************************************
// *****************************************************************************
Point2D Point2D::operator +(const Point2D &other) const {
    return Point2D(pos[0] + other.pos[0], pos[1] + other.pos[1]);
}

// *****************************************************************************
// *****************************************************************************
Point2D Point2D::operator -(const Point2D &other) const {
    return Point2D(pos[0] - other.pos[0], pos[1] - other.pos[1]);
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator +=(const Point2D &other) {
    pos[0] += other.pos[0];
    pos[1] += other.pos[1];
    return *this;
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator -=(const Point2D &other) {
    pos[0] -= other.pos[0];
    pos[1] -= other.pos[1];
    return *this;
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator +=(double other) {
    pos[0] += other;
    pos[1] += other;
    return *this;
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator -=(double other) {
    pos[0] -= other;
    pos[1] -= other;
    return *this;
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator *=(double other) {
    pos[0] *= other;
    pos[1] *= other;
    return *this;
}

// *****************************************************************************
// *****************************************************************************
Point2D& Point2D::operator /=(double other) {
    pos[0] /= other;
    pos[1] /= other;
    return *this;
}

// *****************************************************************************
// *****************************************************************************
void Point2D::print() {
    printf("\nPoint2D (x,y)= (%g,%g)", pos[0], pos[1]);
}

