#include <iostream>
#include "DBERROR.h"

//------------------------------------------------------------------------------
//! Define the static DBError Pointer
//------------------------------------------------------------------------------
DBError* DBError::inst_ = NULL;

//------------------------------------------------------------------------------
//! Private Constructor
//------------------------------------------------------------------------------
DBError::DBError() {
    // Initialize the Attributes
}

//------------------------------------------------------------------------------
//! Destructor
//------------------------------------------------------------------------------
DBError::~DBError() {
    if (inst_ != NULL) {
        delete(inst_);
    }
    // Free the Memory Used by Attributes
}

//------------------------------------------------------------------------------
//! Get the Only Single Instance of DBError
//------------------------------------------------------------------------------
DBError* DBError::getInstance() {
    if (inst_ == NULL) {
        inst_ = new DBError();
    }
    return(inst_);
}

